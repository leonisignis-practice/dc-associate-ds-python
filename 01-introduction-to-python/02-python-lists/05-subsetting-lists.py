fam = ["liz", 1.73, "emma", 1.68, "mom", 1.71, "dad", 1.89]
# zero-indexing
print(fam[0])
print(fam[3])
print(fam[-1])
print(fam[3:6])
print(fam[0:6:2])

# negative indexing
print(fam[7])
print(fam[-1])


# slicing
#    [start : end]
# inclusive : exclusive
print(fam[0:4])
print(fam[:4])
print(fam[4:])
print(fam[:])
print(fam[-4:-1])
